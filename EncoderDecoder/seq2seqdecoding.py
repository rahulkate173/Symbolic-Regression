def generate_predictions_file(model, dataset, save_path="predictions.json"):
    model.eval()
    predictions = []

    # Use the full dataset or validation dataset to see how well it recovered
    print(f"Generating predictions for {len(dataset)} samples...")

    with torch.no_grad():
        for i in tqdm(range(len(dataset)), desc="Inference"):
            data = dataset[i]
            # Prep inputs (add batch dimension)
            emb = data["embedding"].unsqueeze(0).to(device)
            graph = data["graph"].to(device)

            # Start with the SOS token
            input_ids = torch.tensor([[dataset.sos_id]], device=device)

            # Autoregressive generation
            for _ in range(dataset.max_len):
                logits = model(emb, graph, input_ids)
                # Get the last predicted token
                next_token = logits[:, -1, :].argmax(dim=-1).unsqueeze(0)
                input_ids = torch.cat([input_ids, next_token], dim=1)

                # Stop if EOS is reached
                if next_token.item() == dataset.eos_id:
                    break

            # Convert token IDs to Symbols
            # Filter out SOS, EOS, and PAD for the final output string
            pred_tokens = [
                dataset.inv_vocab.get(t.item(), "const(C)")
                for t in input_ids.squeeze()
                if t.item() not in [dataset.sos_id, dataset.eos_id, dataset.pad_id]
            ]

            # Get Ground Truth for comparison
            true_tokens = [
                dataset.inv_vocab.get(t, "const(C)")
                for t in dataset.token_seqs[i]
            ]

            predictions.append({
                "id": i,
                "predicted_skeleton": " ".join(pred_tokens),
                "ground_truth": " ".join(true_tokens)
            })

    with open(save_path, "w") as f:
        json.dump(predictions, f, indent=4)

    print(f"predictions.json created with {len(predictions)} entries.")


