def generate_gp_seeds(model, dataset, num_samples=100):
    model.eval()
    gp_seeds = []

    print(f"Generating {num_samples} GP seeds...")

    with torch.no_grad():
        for i in range(min(num_samples, len(dataset))):
            data = dataset[i]
            emb = data["embedding"].unsqueeze(0).to(device)
            graph = data["graph"].to(device)

            # Start the sequence with the SOS token
            input_ids = torch.tensor([[dataset.sos_id]], device=device)

            # Greedy decoding to build the skeleton
            for _ in range(dataset.max_len):
                logits = model(emb, graph, input_ids)
                # Get the most likely next token
                next_token = logits[:, -1, :].argmax(dim=-1).unsqueeze(0)
                input_ids = torch.cat([input_ids, next_token], dim=1)

                # Stop if we hit the EOS token
                if next_token.item() == dataset.eos_id:
                    break

            # Convert token IDs back to actual math symbols from your vocab
            # We skip SOS, EOS, and PAD to get a clean skeleton
            skeleton_tokens = [
                dataset.inv_vocab.get(t.item(), "const(C)")
                for t in input_ids.squeeze()
                if t.item() not in [dataset.sos_id, dataset.eos_id, dataset.pad_id]
            ]

            gp_seeds.append({
                "sample_id": i,
                "skeleton": " ".join(skeleton_tokens)
            })

    # Save to the json file
    save_path = "gp_seeds.json"
    with open(save_path, "w") as f:
        json.dump(gp_seeds, f, indent=4)

    return save_path

