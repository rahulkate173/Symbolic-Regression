train_loader = GNNDataLoader(train_ds, batch_size=BATCH_SIZE, shuffle=True)
val_loader = GNNDataLoader(val_ds, batch_size=BATCH_SIZE, shuffle=False)

# --- 3. SPARSE ATTENTION MODULE ---
class SparseAttention(nn.Module):
    def __init__(self, embed_dim, num_heads, window_size=8, num_random=4):
        super().__init__()
        self.attn = nn.MultiheadAttention(embed_dim, num_heads, batch_first=True)
        self.window_size = window_size
        self.num_random = num_random

    def forward(self, x):
        B, T, D = x.shape
        mask = torch.full((T, T), float("-inf"), device=x.device)
        for i in range(T):
            # Window Attention
            start = max(0, i - self.window_size)
            mask[i, start:i+1] = 0
            # Random Attention (Global sparse connectivity)
            if self.num_random > 0:
                rand_idx = torch.randint(0, T, (self.num_random,), device=x.device)
                mask[i, rand_idx] = 0
        attn_out, _ = self.attn(x, x, x, attn_mask=mask)
        return attn_out

# --- 4. MODIFIED HYBRID MODEL (GNN Encoder + Sparse Transformer Decoder) ---
class NextGenSymbolicModel(nn.Module):
    def __init__(self, vocab_size, tnet_dim=128, gpt_dim=256, n_layers=3, n_heads=4):
        super().__init__()
        # GNN Structural Encoder
        self.gnn = nn.Sequential(
            nn.Linear(vocab_size + 5, 128),
            nn.ReLU(),
            nn.Dropout(0.2),
            nn.Linear(128, 128)
        )

        # Multimodal Fusion & Embedding
        self.fusion = nn.Linear(tnet_dim + 128, gpt_dim)
        self.token_emb = nn.Embedding(vocab_size + 5, gpt_dim)
        self.pos_emb = nn.Parameter(torch.randn(1, 250, gpt_dim))
        self.dropout = nn.Dropout(0.3)

        # Sparse Transformer Decoder Layers
        self.layers = nn.ModuleList([
            nn.ModuleDict({
                "sparse_attn": SparseAttention(gpt_dim, n_heads),
                "ff": nn.Sequential(
                    nn.Linear(gpt_dim, 4 * gpt_dim),
                    nn.ReLU(),
                    nn.Dropout(0.15),
                    nn.Linear(4 * gpt_dim, gpt_dim)
                ),
                "norm1": nn.LayerNorm(gpt_dim),
                "norm2": nn.LayerNorm(gpt_dim),
            }) for _ in range(n_layers)
        ])

        self.head = nn.Linear(gpt_dim, vocab_size + 5)

    def forward(self, tnet_emb, graph_batch, target_ids):
        # 1. Structural Context from GNN
        struct_feats = self.gnn(graph_batch.x)
        # Global pooling across the batch
        struct_ctx = struct_feats.mean(dim=0).repeat(tnet_emb.size(0), 1)

        # 2. Multimodal Fusion (T-Net Data + GNN Structure)
        # We project the concatenated latent space into the GPT dimension
        fused_context = self.fusion(torch.cat([tnet_emb, struct_ctx], dim=-1)).unsqueeze(1)

        # 3. Target Sequence Embedding
        B, T = target_ids.shape
        tok_embed = self.dropout(self.token_emb(target_ids))

        # Concatenate fused context as a 'prefix' token
        x = torch.cat([fused_context, tok_embed], dim=1)
        x = x + self.pos_emb[:, :T+1, :]

        # 4. Sparse Decoding Layers
        for layer in self.layers:
            attn_out = layer["sparse_attn"](x)
            x = layer["norm1"](x + attn_out)
            ff_out = layer["ff"](x)
            x = layer["norm2"](x + ff_out)

        # Slice out the prefix token to get logits for the sequence
        logits = self.head(x[:, 1:, :])
        return logits

## --- 4. TRAINING WITH VALIDATION & EARLY STOPPING ---
# Ensure NextGenSymbolicModel is initialized with the SparseAttention layers
# as defined in our previous block.
model = NextGenSymbolicModel(
    vocab_size=len(full_dataset.vocab),
    tnet_dim=128,
    gpt_dim=128,      # Reduced to 128 for better generalization
    n_layers=4        # Slimmer model to prevent overfitting
).to(device)

# Regularization-heavy optimizer
optimizer = torch.optim.AdamW(model.parameters(), lr=3e-4, weight_decay=1e-3)
scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(optimizer, mode='min', factor=0.5, patience=3)
# Label smoothing helps the Sparse Attention not over-focus on specific tokens
criterion = nn.CrossEntropyLoss(ignore_index=full_dataset.pad_id, label_smoothing=0.1)

train_losses, val_losses = [], []
best_val_loss = float('inf')
early_stop_counter = 0

for epoch in range(EPOCHS):
    # --- Training Phase ---
    model.train()
    t_loss = 0
    for batch in tqdm(train_loader, desc=f"Epoch {epoch+1}/{EPOCHS}"):
        emb, graph, tgt = batch["embedding"].to(device), batch["graph"].to(device), batch["target_ids"].to(device)

        # Multimodal Augmentation: Add 1% Gaussian noise to PointNet embeddings
        if model.training:
            noise_factor = 0.01 * (1 - (epoch / EPOCHS))
            emb = emb + torch.randn_like(emb) * noise_factor

        # Forward pass: Predicting the skeleton
        logits = model(emb, graph, tgt[:, :-1])
        loss = criterion(logits.reshape(-1, logits.size(-1)), tgt[:, 1:].reshape(-1))

        optimizer.zero_grad()
        loss.backward()
        # Gradient clipping to stabilize sparse attention training
        torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
        optimizer.step()

        t_loss += loss.item()

    # --- Validation Phase ---
    model.eval()
    v_loss = 0
    with torch.no_grad():
        for batch in val_loader:
            emb, graph, tgt = batch["embedding"].to(device), batch["graph"].to(device), batch["target_ids"].to(device)
            # No noise during validation
            logits = model(emb, graph, tgt[:, :-1])
            v_loss += criterion(logits.reshape(-1, logits.size(-1)), tgt[:, 1:].reshape(-1)).item()

    avg_t, avg_v = t_loss / len(train_loader), v_loss / len(val_loader)
    train_losses.append(avg_t)
    scheduler.step(avg_v)
    val_losses.append(avg_v)

    print(f"Loss -> Train: {avg_t:.4f} | Val: {avg_v:.4f} | LR: {scheduler.get_last_lr()[0]:.6f}")

    # --- Checkpointing & Early Stopping ---
    if avg_v < best_val_loss:
        best_val_loss = avg_v
        early_stop_counter = 0
        torch.save({
            'epoch': epoch,
            'model_state_dict': model.state_dict(),
            'optimizer_state_dict': optimizer.state_dict(),
            'loss': best_val_loss,
            'vocab': full_dataset.vocab,
        }, "best_sparse_hybrid_model.pth")
    else:
        early_stop_counter += 1
        if early_stop_counter >= PATIENCE:
            print(f"✅ Early stopping triggered at Epoch {epoch+1}. Model generalized.")
            break

# Final Learning Curve Plot
plt.figure(figsize=(10, 5))
plt.plot(train_losses, label="Train Loss (Augmented)")
plt.plot(val_losses, label="Validation Loss (Generalization)")
plt.xlabel("Epochs")
plt.ylabel("Cross Entropy Loss")
plt.title("Stability Analysis: Sparse GNN-Transformer")
plt.legend()
plt.grid(True)
plt.show()

