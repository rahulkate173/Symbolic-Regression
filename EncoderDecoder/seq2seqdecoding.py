# --- 1. CONFIGURATION ---
device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
EPOCHS = 100
BATCH_SIZE = 16
PATIENCE = 10  # For Early Stopping
LR = 1e-4

# --- 2. DATASET & DATA PREP ---
class HybridSymbolicDataset(Dataset):
    def __init__(self, embedding_path, label_path):
        with open(embedding_path, "r") as f:
            self.embeddings = [torch.tensor(e["embedding"], dtype=torch.float32) for e in json.load(f)]
        with open(label_path, "r") as f:
            data = json.load(f)
            self.token_seqs = data["tokenized_trees"]
            self.vocab = data["vocab"]
            self.inv_vocab = {v: k for k, v in self.vocab.items()}

        self.pad_id = self.vocab.get("<PAD>", max(self.vocab.values()) + 1)
        self.eos_id = self.vocab.get("<EOS>", max(self.vocab.values()) + 2)
        self.sos_id = len(self.vocab) + 2
        self.max_len = max(len(seq) for seq in self.token_seqs) + 2

    def __len__(self): return len(self.embeddings)

    def __getitem__(self, idx):
        emb = self.embeddings[idx]
        tokens = [self.sos_id] + self.token_seqs[idx] + [self.eos_id]
        padded = tokens + [self.pad_id] * (self.max_len - len(tokens))

        # Skeleton Graph (Binary structure)
        edge_index = torch.tensor([[0, 1], [0, 2]], dtype=torch.long)
        node_feats = torch.eye(len(self.vocab) + 5)[tokens[:3]]

        return {
            "embedding": emb,
            "graph": Data(x=node_feats, edge_index=edge_index),
            "target_ids": torch.tensor(padded, dtype=torch.long)
        }

# Initialize Data
full_dataset = HybridSymbolicDataset("/content/tnet_embeddings.json", "/content/tokenized_gpt_labels_with_full_funcs.json")
train_size = int(0.8 * len(full_dataset))
