# --- 1. THE ADVANCED SYMBOLIC MATCHER ---
def advanced_symbolic_check(discovered, ground_truth):
    """
    Checks for structural integrity between discovery and truth.
    Normalizes variable names and verifies core functional operators.
    """
    # Normalize strings: lowercase and remove syntax fluff
    d = str(discovered).lower().replace(" ", "").replace("(", "").replace(")", "").replace(",", "")
    gt = ground_truth.lower().replace(" ", "").replace("<eos>", "").replace("id", "").replace("const", "")

    # Define core physical operators to check for structural match
    operators = ['exp', 'sin', 'cos', 'log', 'pow', 'sqrt', 'add', 'mul', 'div', 'sub']

    # Find which operators actually exist in the ground truth
    gt_ops = [op for op in operators if op in gt]

    if not gt_ops:
        # Fallback for very simple variables/constants
        return 1 if any(var in d for var in ['x0', 'x1', 'x2']) else 0

    # Success criteria:
    # 1. Does the discovered formula contain the same primary operators as the truth?
    # 2. Does it reference the correct number of input variables (features)?
    match_count = sum(1 for op in gt_ops if op in d)

    # We consider it a "Symbolic Recovery" if the core functional
    # skeleton (operators) is at least 80% recovered.
    recovery_ratio = match_count / len(gt_ops)
    return 1 if recovery_ratio >= 0.8 else 0

# --- 2. DATA INTEGRATION ---
def generate_final_gsoc_report(batch_results, predictions_path):
    """
    batch_results: The list of dicts from your GP+BFGS run
    predictions_path: Path to your predictions.json (Ground Truth source)
    """
    with open(predictions_path, "r") as f:
        pred_data = json.load(f)
        # Create a lookup: Row ID -> Ground Truth String
        gt_map = {item["id"]: item["ground_truth"] for item in pred_data}

    final_metrics = []

    for res in batch_results:
        row_id = res["Row"]
        topology = res["Topology"]
        r2 = res["R2"]

        ground_truth = gt_map.get(row_id, "N/A")

        # Calculate Symbolic Recovery
        is_recovered = advanced_symbolic_check(topology, ground_truth)

        final_metrics.append({
            "Row": row_id,
            "Discovered Topology": topology[:45] + "...",
            "Ground Truth Skeleton": ground_truth[:45] + "...",
            "R2 Score": round(r2, 4),
            "Symbolic Recovery": "SUCCESS ✅" if is_recovered else "MISMATCH ❌"
        })

    return pd.DataFrame(final_metrics)

