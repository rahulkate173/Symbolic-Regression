# --- 1. DATA LOADING ---
def load_all_cloud_data(json_path):
    with open(json_path, "r") as f:
        full_list = json.load(f)
    return full_list if isinstance(full_list, list) else [full_list]

# --- 2. AGGRESSIVE PROTECTED FUNCTIONS ---
def _protected_exponent(x):
    # Strictly clip input to prevent e^x from exploding
    return np.exp(np.clip(x, -20, 20))

def _protected_power(x, y):
    # Avoid 0^negative and astronomical powers
    base = np.abs(x)
    exponent = np.clip(y, -5, 5) # Narrower range for stability
    return np.power(base, exponent)

def _protected_div(x, y):
    return np.where(np.abs(y) > 0.001, np.divide(x, y), 1.0)

exp_func = make_function(function=_protected_exponent, name='exp', arity=1)
pow_func = make_function(function=_protected_power, name='pow', arity=2)
div_func = make_function(function=_protected_div, name='div', arity=2)

# --- 3. BATCH PROCESSOR WITH OUTPUT CAPPING ---
def run_stable_discovery(json_path):
    full_cloud_list = load_all_cloud_data(json_path)
    results = []

    for i, row_data in enumerate(full_cloud_list):
        data_points = np.array(row_data["data"])
        X, y = data_points[:, :-1], data_points[:, -1]

        est = SymbolicRegressor(
            population_size=1000, generations=15,
            function_set=['add', 'sub', 'mul', div_func, 'sin', 'cos', 'log', exp_func, pow_func],
            metric='rmse', parsimony_coefficient=0.01, # Increased penalty to prevent deep/unstable trees
            random_state=42, verbose=0
        )

        try:
            est.fit(X, y)
            best_prog = est._program

            # Prediction with Global Clipping
            y_pred = est.predict(X)
            # Replace NaNs/Infs and cap values at reasonable physical limits
            y_pred = np.nan_to_num(y_pred, nan=0.0, posinf=1e10, neginf=-1e10)

            r2 = r2_score(y, y_pred)
            results.append({"Row": i, "R2": r2, "Formula": str(best_prog)[:50]})
            print(f"Row {i} | R2: {max(0, r2):.4f}") # Cap R2 display at 0 for readability

        except Exception as e:
            print(f"Row {i} Failed: {e}")

    return pd.DataFrame(results)

